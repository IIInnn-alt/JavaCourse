package com.base.base;

// 数据类型
public class demo01 {
    public static void main(String[] args) {
        //八大基本数据类型
        //整数
        int num1 = 200;
        byte num2 = 20;
        short num3 = 300;
        long num4 = 500L; //Long类型要在后面加个L
        //小数: 浮点数
        float num5 = 50.1F;//float类型要在小数后面加个F
        double num6 = 3.1415927;
        //字符 char
        char name = '字'; //一个字符
        //字符串 String 不是关键字 ，是类 String namea = '名字'
        //布尔值:是非
        boolean flag = true; //false



        String a = "10";
        int b = 10;
        System.out.println(a);
        System.out.println(b);
    }
}
